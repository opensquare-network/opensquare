/// Configure initial storage state for FRAME modules.
fn testnet_genesis(
    wasm_binary: &[u8],
    initial_authorities: Vec<(AuraId, GrandpaId)>,
    root_key: AccountId,
    endowed_accounts: Vec<AccountId>,
) -> GenesisConfig {
    GenesisConfig {
        frame_system: Some(SystemConfig {
            // Add Wasm runtime to storage.
            code: wasm_binary.to_vec(),
            changes_trie_config: Default::default(),
        }),
        pallet_balances: Some(BalancesConfig {
            // Configure endowed accounts with initial balance of 1 << 60.
            balances: endowed_accounts
                .iter()
                .cloned()
                .map(|k| (k, 1 << 60))
                .collect(),
        }),
        pallet_aura: Some(AuraConfig {
            authorities: initial_authorities.iter().map(|x| (x.0.clone())).collect(),
        }),
        pallet_grandpa: Some(GrandpaConfig {
            authorities: initial_authorities
                .iter()
                .map(|x| (x.1.clone(), 1))
                .collect(),
        }),
        pallet_sudo: Some(SudoConfig {
            // Assign network admin rights.
            key: root_key.clone(),
        }),
        orml_oracle: Some(OracleConfig {
            members: Default::default(), // initialized by OperatorMembership
            phantom: Default::default(),
        }),
        orml_tokens: Some(TokensConfig {
            endowed_accounts: vec![],
        }),
        ospallet_system: Some(OsSystemConfig {
            tmp_council: vec![root_key],
        }),
        ospallet_bounties: Some(OsBountiesConfig {
            currency_ratios: vec![
                (CurrencyId::Native, 1u128),
                (CurrencyId::USDT, 1u128),
                (CurrencyId::AUSD, 1u128),
                (CurrencyId::DOT, 5u128),
            ],
            dummy: 0u32,
        }),
    }
}
